/**
 * 将二维数组（行 → 字节数组）合并为一个 Buffer。
 * 每行之间用换行符 0x0A 连接，空行保留。
 */
function linesToBuffer(lines) {
  const parts = [];
  for (let i = 0; i < lines.length; i++) {
    if (i > 0) parts.push(Buffer.from([0x0A])); // 行间换行
    if (lines[i].length > 0) {
      parts.push(Buffer.from(lines[i]));
    }
  }
  return Buffer.concat(parts);
}

/**
 * 对单行字节数组做检测（如果该行含高位字节的话）。
 */
function detectLine(bytes) {
  if (!bytes || bytes.length === 0) return null;
  const buf = Buffer.from(bytes);
  return jschardet.detect(buf);
}

