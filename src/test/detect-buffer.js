/**
 * 检测 Buffer 的字符编码
 *
 * @param {Buffer} buf - 要检测的 Buffer
 * @returns {{ encoding: string|null, confidence: number }} 检测结果
 *
 * @example
 *   const fs = require('fs');
 *   const buf = fs.readFileSync('some-file.txt');
 *   const result = detectBuffer(buf);
 *   console.log(result);
 *   // { encoding: 'utf-8', confidence: 0.99 }
 */
function detectBuffer(buf) {
  if (!Buffer.isBuffer(buf)) {
    throw new TypeError('参数必须是 Buffer');
  }
  return jschardet.detect(buf);
}

